#include <iostream>

using namespace std;

int main() {
    char inch;
    
    // Prompting the user to input a character
    cout << "Enter a character: ";
    cin >> inch;
    
    // Displaying information about the character
    cout << "Character entered: " << a << endl;
    cout << "ASCII code: " << static_cast<int>(inch) << endl;

    return 0;
}

